export class Sumas{

    sumaDousNumeros(a,b){
        return a+b;
    }

    sumaTresNumeros(a,b,c){
        return a+b+c;
    }

}


export class Restas{
    restaDousNumeros(a,b){
        return a-b;
    }
}
